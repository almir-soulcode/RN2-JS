resultado = num1 - num2;
console.log(resultado);

resultado = num1 * num2;
console.log(resultado);

resultado = num1 / num2;
console.log(resultado);

resultado = num1 ** 2;
console.log(resultado);

resultado = num2 % num1;
console.log(resultado);